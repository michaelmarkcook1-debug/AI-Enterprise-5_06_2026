// Build-time codegen: snapshot the migration names the CODE ships with.
// ─────────────────────────────────────────────────────────────────────
// Runtime cannot reliably read prisma/migrations/ (the dir isn't bundled into
// the serverless function), so we freeze the expected migration list into a TS
// module at build time. lib/db/migration-drift.ts compares this against what's
// actually applied in the live DB and fails loud on drift.
//
// Run automatically in the Vercel build (see vercel.json) BEFORE next build, and
// committed so local `tsc`/imports resolve. Plain Node ESM — no tsx needed.

import { readdirSync, existsSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const migrationsDir = join(root, "prisma", "migrations");
const outFile = join(root, "lib", "db", "expected-migrations.ts");

// Prisma migration folders are timestamp-prefixed, so lexical sort = chronological.
const names = readdirSync(migrationsDir, { withFileTypes: true })
  .filter((d) => d.isDirectory() && existsSync(join(migrationsDir, d.name, "migration.sql")))
  .map((d) => d.name)
  .sort();

const body = `// AUTO-GENERATED by scripts/generate-expected-migrations.mjs — DO NOT EDIT BY HAND.
// Regenerated in the Vercel build before \`next build\`. Lists the Prisma migrations
// this code expects to be applied; lib/db/migration-drift.ts compares it to the
// live DB so a lagging production database fails loud instead of breaking silently.
// (Regenerate locally with: node scripts/generate-expected-migrations.mjs)

export const EXPECTED_MIGRATIONS: readonly string[] = [
${names.map((n) => `  "${n}",`).join("\n")}
];

/** The newest migration the code ships with (last in chronological order). */
export const LATEST_EXPECTED_MIGRATION = ${names.length ? `"${names[names.length - 1]}"` : "null"} as const;
`;

writeFileSync(outFile, body);
console.log(`[expected-migrations] wrote ${names.length} migrations → ${outFile}`);
